              p <b>Disponibilidad:</b> garantiza que los sistemas y datos sean accesibles cuando los usuarios autorizados los necesiten. Este principio es clave para asegurar que los servicios digitales estén siempre operativos, incluso frente a ataques. La protección contra ataques de denegación de servicio (DoS) o ataques DDoS es un ejemplo claro, ya que estos ataques buscan interrumpir la disponibilidad de un servicio online al sobrecargarlo con tráfico malicioso. Mediante la implementación de infraestructuras de respaldo y sistemas de redundancia, las organizaciones pueden mantener la disponibilidad de sus servicios incluso en situaciones críticas.

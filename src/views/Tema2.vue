              p <b>Integridad:</b> asegura que la información no sea alterada de manera no autorizada. Esto implica que los datos sean precisos y completos, sin modificaciones maliciosas o accidentales. Se emplean mecanismos como las firmas digitales o códigos hash para verificar que los datos no hayan sido alterados. Por ejemplo, si un archivo es enviado a través de internet, su integridad se puede verificar mediante un código hash, lo que permite comprobar si el archivo ha sido modificado durante el tránsito.
